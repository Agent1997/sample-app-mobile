function addXY(actionData, x, y) {
	return actionData.map(single => {
		Object.entries(single).forEach(([ key, value ]) => {
			if (typeof value === 'number' && (key === 'x' || key === 'y')) {
				single[ key ] = key === 'x' ? value + x : value + y;
				return;
			}
			single[ key ] = value;
		});
		return single;
	});
}
